package day09_string_methods_for_loop;

public class ForLoopQuestion2 {

	public static void main(String[] args) {
		/*
		 * Print on the console all integers from 100 to 1 in the same line with space
		 * between them like; 100 98 96 94 . . . 4 2 0
		 */
		
		for (int i = 100; i >=0; i-=2) {
			System.out.print(i+" ");
			
		}
	}

}
